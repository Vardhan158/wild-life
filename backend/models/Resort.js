import mongoose from "mongoose";

const resortSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    type: {
      type: String,
      required: true,
    },
    price: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      default: 0,
    },
    reviews: {
      type: Number,
      default: 0,
    },
    image: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
    amenities: [String],
    facilities: [String],
    roomTypes: [
      {
        type: String,
        capacity: Number,
        price: String,
        beds: Number,
      },
    ],
    roomsAvailable: Number,
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  { timestamps: true }
);

const Resort = mongoose.model("Resort", resortSchema);
export default Resort;
